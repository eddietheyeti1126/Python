def birthday():
    """
    Returns a string with the user's birthday in the format:
    'Your birthday is Month Day, Year.'
    """
    return "Your birthday is July 8, 1998."

def address():
    """
    Returns a string with the user's address in the format:
    'Your address is Street, City, State, ZIP Code.'
    """
    return "Your address is 123 Main St, Crystal Lake, IL, 60013."

# Print the results
print(birthday())
print(address())
